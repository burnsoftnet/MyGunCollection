    /// <summary>
    /// Class Contacts handling class
    /// </summary>
    public class Shops
    {
        #region "Exception Error Handling"        
        /// <summary>
        /// The class location
        /// </summary>
        private static string ClassLocation = "BurnSoft.Applications.MGC.PeopleAndPlaces.Shops";
        /// <summary>
        /// Errors the message for regular Exceptions
